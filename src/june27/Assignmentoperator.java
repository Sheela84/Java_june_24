package june27;

public class Assignmentoperator {
    public static void main(String[] args){
        int x=10;
        int y=20;
        int z=30;
        System.out.println(x<y && x>z);
        System.out.println(x<y && x<z);
        System.out.println(x>y || x>z);
        System.out.println(x<y || x>z);
    }
}
